#The formula to calculate circle area is Area = pi * radius²
pi = 3.14
radius = float(input("Enter the radius: "))
#To elevate numbers we use double asterisk(**) on python, where the number at right is the exponent

area = pi * (radius**2)
#"um" is Unit of Measurement
print(f'The area of a circle with Radius: {radius}, is Area: {area}um²')

